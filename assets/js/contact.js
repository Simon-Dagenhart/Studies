(function () {
    var form = document.getElementById('contactForm');
    if (!form) return;

    var successAlert = document.getElementById('successAlert');
    var errorAlert = document.getElementById('errorAlert');
    var courseSelect = document.getElementById('course');

    // Pre-select the course of interest when arriving from a course's "Enquire" link.
    var params = new URLSearchParams(window.location.search);
    var courseParam = params.get('course');
    if (courseParam && courseSelect.querySelector('option[value="' + CSS.escape(courseParam) + '"]')) {
        courseSelect.value = courseParam;
    }

    function isValidEmail(value) {
        return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(value);
    }

    // Form-validation and success copy, in both languages — read at submit
    // time via document.documentElement.lang (set by the language toggle
    // in main.js) since this text is generated by JS, not present in HTML.
    var MESSAGES = {
        en: {
            nameRequired: 'Name is required.',
            emailRequired: 'A valid email is required.',
            messageRequired: 'Please include a short message.',
            success: function (name) { return 'Thanks, ' + name + '! Your message has been received — I\'ll be in touch soon.'; }
        },
        ar: {
            nameRequired: 'الاسم مطلوب.',
            emailRequired: 'يرجى إدخال بريد إلكتروني صحيح.',
            messageRequired: 'يرجى كتابة رسالة قصيرة.',
            success: function (name) { return 'شكرًا لك يا ' + name + '! تم استلام رسالتك — سأتواصل معك قريبًا.'; }
        }
    };

    form.addEventListener('submit', function (e) {
        e.preventDefault();

        var lang = document.documentElement.lang === 'ar' ? 'ar' : 'en';
        var t = MESSAGES[lang];

        var name = form.name.value.trim();
        var email = form.email.value.trim();
        var message = form.message.value.trim();
        var errors = [];

        if (name === '') errors.push(t.nameRequired);
        if (email === '' || !isValidEmail(email)) errors.push(t.emailRequired);
        if (message === '') errors.push(t.messageRequired);

        errorAlert.innerHTML = '';
        successAlert.hidden = true;

        if (errors.length) {
            errors.forEach(function (err) {
                var li = document.createElement('li');
                li.textContent = err;
                errorAlert.appendChild(li);
            });
            errorAlert.hidden = false;
            return;
        }

        errorAlert.hidden = true;
        // Demo only: this form isn't wired to a backend, so nothing is actually
        // sent. Hook this up to a form service (e.g. Formspree) or your own
        // endpoint to receive real submissions.
        successAlert.textContent = t.success(name);
        successAlert.hidden = false;
    });
})();
